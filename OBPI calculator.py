def OBPIratio(포트폴리오금액, 옵션프리미엄, 옵션델타):
    s=(포트폴리오금액-옵션프리미엄)*(1-abs(옵션델타))
    주식투자비율=s/포트폴리오금액*100
    채권투자비율=100-주식투자비율
    return 주식투자비율, 채권투자비율
def OBPIamount(포트폴리오금액, 옵션프리미엄, 옵션델타):
    주식투자액=(포트폴리오금액-옵션프리미엄)*(1-abs(옵션델타))
    채권투자액=포트폴리오금액-주식투자액
    return 주식투자액, 채권투자액
print(OBPIratio(200,8,-0.3))
print(OBPIamount(200,8,-0.3))
print(OBPIamount(100,4,-0.3))